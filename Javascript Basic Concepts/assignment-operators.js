// Assignment operators assign values to JavaScript variables.

// use (=) for assaign value

let a = 200;
a += 300; // same as a = a + 300
console.log(a);

a -= 250; // same as a = a - 250
console.log(a);

a *= 3; // same as a= a * 3
console.log(a);

a /= 5; // same as a = a / 5
console.log(a);

a %= 7; // same as a = a % 7
console.log(a);

a **= 2; // same as a = a ** 2
console.log(a);
