/** ######################### Number methods - MAX_VALUE #########################
 * * Number.MAX_VALUE returns the largest number possible in JavaScript.
 * * Number.MAX_VALUE has the value of 1.7976931348623157e+308.
 * * MAX_VALUE is a property of the JavaScript Number object.
 */

console.log(Number.MAX_VALUE);

// Using x.MAX_VALUE, where x is a variable, will return undefined
let x = 10;
console.log(x.MAX_VALUE);

/** ######################### Number methods - MIN_VALUE #########################
 * * Number.MIN_VALUE returns the smallest number possible in JavaScript.
 * * Number.MIN_VALUE has a value of 5e-324.
 */
//  Using x.MIN_VALUE, where x is a variable, will return undefined
console.log(Number.MIN_VALUE);
console.log(x.MIN_VALUE);

/** ######################### Number methods - NEGATIVE_INFINITY #########################
 * * Number.NEGATIVE_INFINITY returns negative infinity.
 * * Number.NEGATIVE_INFINITY is "a number lower than any other number.
 * * Using x.NEGATIVE_INFINITY, where x is a variable, will return undefined
 */

console.log(Number.NEGATIVE_INFINITY);
console.log(-Number.MAX_VALUE * 2);
console.log(x.NEGATIVE_INFINITY);

/** ######################### Number methods - POSITIVE_INFINITY #########################
 * * Number.POSITIVE_INFINITY returns positive infinity.
 * * Number.POSITIVE_INFINITY is "a number higher than any other number.
 * * Using x.POSITIVE_INFINITY, where x is a variable, will return undefined
 */

console.log(Number.POSITIVE_INFINITY);
console.log(Number.MAX_VALUE * 2);
console.log(x.POSITIVE_INFINITY);
