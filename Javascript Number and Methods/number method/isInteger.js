/** ######################### Numbers methods - isInteger(value) #########################
 * * The Number.isInteger() method returns true if a number is a integer number.

 */

console.log(Number.isInteger(123));
console.log(Number.isInteger(-123));
console.log(Number.isInteger("-123"));
console.log(Number.isInteger(4 - 2));
console.log(Number.isInteger(4 / 2));
console.log(Number.isInteger(-123 / 0));
console.log(Number.isInteger(0.5));
console.log(Number.isInteger(0));
console.log(Number.isInteger(5 / 2));
console.log(Number.isInteger(false));
console.log(Number.isInteger(NaN));
console.log(Number.isInteger(Infinity));
console.log(Number.isInteger(-Infinity));
