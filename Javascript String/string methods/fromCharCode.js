/** ######################### string Methods - fromCharCode() #########################
 * * The String.fromCharCode() method converts Unicode values to characters.
 * * The String.fromCharCode() is a static method of the String object.
 * * The syntax is always String.fromCharCode().
 */


let char= String.fromCharCode(87);
console.log(char);

console.log(String.fromCharCode(88));

let text = String.fromCharCode(72,101,108,108,111,32,119,111,114,108,100)
console.log(text);