/** ######################### Array.isArray() #########################
 * * The isArray() method returns true if an object is an array, otherwise false.
 * *
 */

let fruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(Array.isArray(fruits));
let test = "Thea";
console.log(Array.isArray(test));

